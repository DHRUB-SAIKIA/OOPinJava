public class MethodMultipleParametersQ6
{
    static void myMethod(String fname, int age)
    {
        System.out.println(fname + " is " + age);
    }
    public static void main(String[ ] args)
    {
        myMethod("Dhruba", 19);
        myMethod("Anurag", 20);
        myMethod("Anmol", 21);
    }
}
